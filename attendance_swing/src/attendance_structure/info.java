package attendance_structure;

// this is our info abstract class
public abstract class info {

    // this is just a comment for our abstract class which is going to be very helpful
    // it is extended by both student and teacher since they both have attributes in common
    private String name;
    private String email;
    private String address;
    private String code;
    private String username;
    private String password;

    // simple getters and setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }


}
